    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (this.index > -1) {
            this.examEditorFrm.getQuestions().remove(this.index);
        }
        this.examEditorFrm.refreshTable();
        this.dispose();
    }//GEN-LAST:event_btnDeleteActionPerformed

    public void setField(JTextComponent textField, String name, Question question, BiConsumer<Question, String> setter) {
        textField.requestFocus();
        if (Utils.isBlank(textField.getText())) {
            throw new RuntimeException(String.format("%s bo≈ü!", name));
        }
        setter.accept(question, textField.getText());

    }

    public void setIntegerField(JTextField textField, String name, Question question, BiConsumer<Question, Integer> setter) {
        textField.requestFocus();
        if (Utils.isBlank(textField.getText())) {
            throw new RuntimeException(String.format("%s bo≈ü!", name));
        }
        try {
            Integer intText = Integer.valueOf(textField.getText());
            setter.accept(question, intText);
        } catch (NumberFormatException ex) {
            throw new RuntimeException(String.format("%s sayƒ±sal deƒüil", name));
        }
    }

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        Question question = new Question();
        try {
            Utils.setField(this.txtQuestion, this.lblQuestion.getText(), question, Question::setQuestion);
            Utils.setField(this.txtAnswer, this.lblAnswer.getText(), question, Question::setAnswer);
            Utils.setIntegerField(this.txtBleed, this.lblBleed.getText(), question, Question::setBleed);
            Utils.setIntegerField(this.txtTime, this.lblTime.getText(), question, Question::setScreenSeconds);
            Utils.setIntegerField(this.txtPenalty, this.lblPenalty.getText(), question, Question::setPenalty);
            Utils.setIntegerField(this.txtBonus, this.lblBonus.getText(), question, Question::setBonus);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
            return;
        }
        if (this.index == -1) {
            this.examEditorFrm.getQuestions().add(question);
        } else {
            this.examEditorFrm.getQuestions().set(this.index, question);
        }
        this.examEditorFrm.refreshTable();
        this.dispose();
    }//GEN-LAST:event_btnSaveActionPerformed

